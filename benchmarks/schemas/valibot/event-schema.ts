import * as v from "valibot";

export const analyticsEventSchema = v.object({
    eventId: v.string(),
    name: v.string(),
    anonymousId: v.string(),
    sessionId: v.string(),
    timestamp: v.number(),
    sessionStartTime: v.number(),
    referrer: v.string(),
    path: v.string(),
    title: v.string(),
    screen_resolution: v.string(),
    viewport_size: v.string(),
    language: v.string(),
    timezone: v.string(),
    connection_type: v.string(),
    rtt: v.number(),
    downlink: v.number(),
    time_on_page: v.number(),
    scroll_depth: v.number(),
    interaction_count: v.number(),
    page_count: v.number(),
    utm_source: v.string(),
    utm_medium: v.string(),
    utm_campaign: v.string(),
    utm_term: v.string(),
    utm_content: v.string(),
    load_time: v.number(),
    dom_ready_time: v.number(),
    dom_interactive: v.number(),
    ttfb: v.number(),
    render_time: v.number(),
    redirect_time: v.number(),
    domain_lookup_time: v.number(),
    href: v.string(),
    text: v.string(),
    value: v.string(),
    properties: v.record(v.string(), v.any()),
});

export type AnalyticsEvent = v.InferOutput<typeof analyticsEventSchema>;
