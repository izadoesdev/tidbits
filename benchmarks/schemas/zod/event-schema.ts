import z from 'zod';

export const analyticsEventSchema = z.object({
    eventId: z.string(),
    name: z.string(),
    anonymousId: z.string(),
    sessionId: z.string(),
    timestamp: z.number(),
    sessionStartTime: z.number(),
    referrer: z.string(),
    path: z.string(),
    title: z.string(),
    screen_resolution: z.string(),
    viewport_size: z.string(),
    language: z.string(),
    timezone: z.string(),
    connection_type: z.string(),
    rtt: z.number(),
    downlink: z.number(),
    time_on_page: z.number(),
    scroll_depth: z.number(),
    interaction_count: z.number(),
    page_count: z.number(),
    utm_source: z.string(),
    utm_medium: z.string(),
    utm_campaign: z.string(),
    utm_term: z.string(),
    utm_content: z.string(),
    load_time: z.number(),
    dom_ready_time: z.number(),
    dom_interactive: z.number(),
    ttfb: z.number(),
    render_time: z.number(),
    redirect_time: z.number(),
    domain_lookup_time: z.number(),
    href: z.string(),
    text: z.string(),
    value: z.string(),
    properties: z.record(z.string(), z.any()),
});

export type AnalyticsEvent = z.infer<typeof analyticsEventSchema>;
